  function normalizeLocale(locale) {
    // 这一段只接受内置语言，避免本地存储或云同步写入任意 locale 影响渲染。
    // Accept only bundled locales so local storage or cloud sync cannot inject arbitrary locale values.
    const value = String(locale || "").trim();
    return supportedLocaleSet.has(value) ? value : defaultLocale;
  }

  function resolveLocale(candidate) {
    // 这一段优先使用显式传入值，其次读设置模块，最后回退到默认中文。
    // Prefer an explicit candidate, then settings, and finally fall back to default Chinese.
    if (candidate) return normalizeLocale(candidate);
    const settingsLocale = runtime.systemModules.settingsMenu?.settings?.getSettings?.()?.uiLanguage;
    activeLocale = normalizeLocale(settingsLocale || activeLocale);
    return activeLocale;
  }

  function setLocale(locale) {
    // 这一段缓存当前语言，供设置模块尚未可读的早期渲染路径使用。
    // Cache the current locale for early render paths before settings are readable.
    activeLocale = normalizeLocale(locale);
    return activeLocale;
  }

